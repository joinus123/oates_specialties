                <p>As with all <span>natural rubber products</span>, proper care should be given to assure the longevity and safety of your purchase.</p>
                <ul>
                   <li>
                      <p>
                         Prevent exposure to sharp points or edges.  Nicks, cuts, and abrasions, which are exacerbated as the tubing is stretched, is the primary cause of tubing failure.  These events occur most often from athletes stepping on the tubing, exposure to sharp points or being pinched in equipment bags, or from the tubing rubbing against chain link fences.  As a safeguard, users should routinely inspect the tubing for nicks, cuts, and abrasions before every use.  If any such issues are found, use of the tubing should be discontinued immediately.
                      </p>
                   </li>
                   <li>
                      <p>Do not allow the resistance tubing to be over extended; stretching resistance tubing too far can lead to early fatigue and product failure.  The ideal distance for Oates Specialties’ tubing to be used is with exercises that terminate no more than 12 feet from the tubing anchor. This resistance tubing should never be stretched more than 12 feet from its anchor. </p>
                   </li>
                   <li>
                      <p>
